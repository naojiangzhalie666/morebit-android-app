package com.zjzy.morebit.pojo;

public class ActivityLinkBean {
    private String activityId;
    private String activityTitle;
    private String activityLink;
    private String activityLinkTkl;

    public String getActivityId() {
        return activityId;
    }

    public void setActivityId(String activityId) {
        this.activityId = activityId;
    }

    public String getActivityTitle() {
        return activityTitle;
    }

    public void setActivityTitle(String activityTitle) {
        this.activityTitle = activityTitle;
    }

    public String getActivityLink() {
        return activityLink;
    }

    public void setActivityLink(String activityLink) {
        this.activityLink = activityLink;
    }

    public String getActivityLinkTkl() {
        return activityLinkTkl;
    }

    public void setActivityLinkTkl(String activityLinkTkl) {
        this.activityLinkTkl = activityLinkTkl;
    }
}
